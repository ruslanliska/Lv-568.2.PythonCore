"""This is defining variables and outputting results."""

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))
print('a =', a)
print('b =', b)
print('Result (a+b):', a + b)
print('Result (a-b):', a - b)
print('Result (a/b):', a / b)
print('Result (a*b):', a * b)
print('Result (a**b):', a ** b)
print('Result (a%b):', a % b)
